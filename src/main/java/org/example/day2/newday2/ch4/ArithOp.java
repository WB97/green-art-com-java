package org.example.day2.newday2.ch4;

public class ArithOp {
    public static void main(String[] args) {
        int n1 = 7;
        int n2 = 3;

        System.out.printf("%d + %d = %d\n", n1, n2, (n1+n2));
        System.out.printf("%d - %d = %d\n", n1, n2, (n1-n2));
        System.out.printf("%d * %d = %d\n", n1, n2, (n1*n2));
        System.out.printf("%d / %d = %d\n", n1, n2, (n1/n2));
        System.out.printf("%d %% %d = %d\n", n1, n2, (n1%n2));
    }
}
