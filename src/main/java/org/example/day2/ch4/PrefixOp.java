package org.example.day2.ch4;

public class PrefixOp {
    public static void main(String[] args) {
        int num = 7;
        System.out.println("num : " + num);
        num++;

        System.out.println(num);

        float num2 = 7;

        ++num2;
        System.out.println(++num2);
    }
}
