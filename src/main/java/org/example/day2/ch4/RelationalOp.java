package org.example.day2.ch4;

public class RelationalOp {
    public static void main(String[] args) {
        System.out.println("3 >= 2 : " + (3 >= 2));
        System.out.println("3 >= 3 : " + (3 >= 3));
        System.out.println("3 > 3 : " + (3 > 3));
        System.out.println("3 < 3 : " + (3 < 3));
        System.out.println("1 < 3 : " + (1 < 3));
        System.out.println("1 <= 3 : " + (1 <= 3));

        System.out.println("3 == 3 : " + (3 == 3));
        System.out.println("3 == 4 : " + (3 != 4));
    }
}
